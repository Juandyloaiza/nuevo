let edad=parseInt(prompt("introduzca su edad"));
switch (edad) {
    case 15:
        console.log("tiene 15 años");
        break;
    case 20:
        console.log("tiene 20 años");
        break;
    case 30:
        console.log("tiene 30 años");
        break;
    default:
        console.log("ingrese una edad valida");
        break;
}